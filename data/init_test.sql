CREATE TABLE IF NOT EXISTS FFR_Scores(
  ScoreHash TEXT PRIMARY KEY NOT NULL,
  UserID INTEGER NOT NULL,
  Quarantine INTEGER NOT NULL,
  Player TEXT NOT NULL,
  Title TEXT NOT NULL,
  Artist TEXT NOT NULL,
  Creator TEXT NOT NULL,
  W0 INTEGER NOT NULL,
  W1 INTEGER NOT NULL,
  W2 INTEGER NOT NULL,
  W3 INTEGER NOT NULL,
  W4 INTEGER NOT NULL,
  W5 INTEGER NOT NULL,
  Equiv FLOAT NOT NULL,
  Raw FLOAT NOT NULL,
  TimeAdded TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP,
  TimeAchieved TEXT NOT NULL
);
CREATE TABLE IF NOT EXISTS Users(
    UserID INTEGER PRIMARY KEY NOT NULL,
    NameOsu TEXT,
    ProfileOsu TEXT,
    NameEtterna TEXT,
    ProfileEtterna TEXT,
    NameQuaver TEXT,
    ProfileQuaver TEXT,
    NameFFR TEXT,
    ProfileFFR TEXT,
    NameRobeats TEXT,
    ProfileRobeats TEXT,
    Group INT NOT NULL DEFAULT 1,
    Regdate TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP,
    SessionHistory JSON NOT NULL DEFAULT {}
);